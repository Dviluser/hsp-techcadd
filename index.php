<script>
(function () {
    "use strict";

    document.addEventListener("DOMContentLoaded", function () {
        initEnhancedCounters();
    });

    /* -------------------------
       COUNTER FIXED FUNCTION
    ----------------------------- */
    function initEnhancedCounters() {
        const counters = document.querySelectorAll(".counter-number");

        const observer = new IntersectionObserver(
            (entries) => {
                entries.forEach((entry) => {
                    if (entry.isIntersecting) {
                        startCounter(entry.target);
                        observer.unobserve(entry.target);
                    }
                });
            },
            { threshold: 0.6 }
        );

        counters.forEach((counter) => observer.observe(counter));
    }

    function startCounter(counter) {
        let target = parseInt(counter.getAttribute("data-target"));
        let suffix = counter.getAttribute("data-suffix") || "";
        let duration = 2000;
        let start = 0;
        let startTime = null;

        function updateCounter(timestamp) {
            if (!startTime) startTime = timestamp;

            let progress = (timestamp - startTime) / duration;
            let eased = progress * (2 - progress);
            if (progress > 1) eased = 1;

            let current = Math.floor(start + (target - start) * eased);

            counter.textContent = formatNumber(current, suffix);

            if (progress < 1) {
                requestAnimationFrame(updateCounter);
            } else {
                counter.textContent = formatNumber(target, suffix);
            }
        }

        requestAnimationFrame(updateCounter);
    }

    /* -------------------------
       NUMBER FORMATTER FIXED
    ----------------------------- */
    function formatNumber(value, suffix) {
        if (suffix === "k+") return (value / 1000).toFixed(0) + "k+";
        return value + suffix;
    }
})();
