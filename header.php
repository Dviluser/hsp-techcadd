            position: absolute;
            border-radius: 50%;
            background: rgba(100, 33, 255, 0.2);
            transform: scale(0);
            animation: ripple 0.6s linear;
            width: ${size}px;
            height: ${size}px;
            left: ${x}px;
            top: ${y}px;
            pointer-events: none;
        `;

