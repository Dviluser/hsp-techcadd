        fetch('new-submit.php', {
          method: 'POST',
          body: formData
        })
        .then(response => response.json())
        .then(data => {
          if (data.success) {
            // Show success message
            submitBtn.innerHTML = '<i class="fas fa-check-circle" style="color:#22c55e;"></i> Submitted!';
