function scrollSlider(wrapperId, speed = 1, direction = "left") {
  const wrapper = document.getElementById(wrapperId);
  let scrollAmount = 0;

  function animate() {
    const totalWidth = wrapper.scrollWidth / 2; // Because we duplicated slides
    if (direction === "left") {
      scrollAmount += speed;
      if (scrollAmount >= totalWidth) scrollAmount = 0;
    } else {
      scrollAmount -= speed;
      if (scrollAmount <= 0) scrollAmount = totalWidth;
    }
    wrapper.style.transform = `translateX(-${scrollAmount}px)`;
    requestAnimationFrame(animate);
  }

  animate();
}

